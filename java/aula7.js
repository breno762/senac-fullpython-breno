let numero1 = parseInt(prompt("Digite um número para a contagem regressiva"))
for (let i = numero1; i >= 0; i--) {
    console.log(i);
}

let numero2 = parseInt(prompt("Digite um número"))
let soma = 0;
let indice = 1;
while (indice <= numero2) {
    soma += indice;
    indice++;
}
console.log("Soma:", soma);

